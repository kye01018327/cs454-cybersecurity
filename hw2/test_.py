from AES import *
from utils import *

textbook_plaintext = 0x0123456789abcdeffedcba9876543210
textbook_key = 0x0f1571c947d9e8590cb7add6af7f6798
textbook_ciphertext = 0xff0b844a0853bf7c6934ab4364148fb9


def test_int_to_matrix():
    input_val = textbook_plaintext
    output = [
        [0x01, 0x89, 0xfe, 0x76],
        [0x23, 0xab, 0xdc, 0x54],
        [0x45, 0xcd, 0xba, 0x32],
        [0x67, 0xef, 0x98, 0x10]
    ]
    assert convert_int_to_matrix(input_val) == output
    assert convert_matrix_to_int(output) == input_val

def test_add_round_key():
    state = convert_int_to_matrix(textbook_plaintext)
    key = convert_int_to_matrix(textbook_key)
    expected_output = [
        [0x0e, 0xce, 0xf2, 0xd9],
        [0x36, 0x72, 0x6b, 0x2b],
        [0x34, 0x25, 0x17, 0x55],
        [0xae, 0xb6, 0x4e, 0x88]
    ]
    state = add_round_key(state, key)
    assert state == expected_output

def test_sub_bytes():
    state = [
        [0x0e, 0xce, 0xf2, 0xd9],
        [0x36, 0x72, 0x6b, 0x2b],
        [0x34, 0x25, 0x17, 0x55],
        [0xae, 0xb6, 0x4e, 0x88]
    ]
    expected_output = [
        [0xab, 0x8b, 0x89, 0x35],
        [0x05, 0x40, 0x7f, 0xf1],
        [0x18, 0x3f, 0xf0, 0xfc],
        [0xe4, 0x4e, 0x2f, 0xc4]
    ]
    state = sub_bytes(state)
    assert state == expected_output

def test_shift_rows():
    state = [
        [0xab, 0x8b, 0x89, 0x35],
        [0x05, 0x40, 0x7f, 0xf1],
        [0x18, 0x3f, 0xf0, 0xfc],
        [0xe4, 0x4e, 0x2f, 0xc4]
    ]
    expected_output = [
        [0xab, 0x8b, 0x89, 0x35],
        [0x40, 0x7f, 0xf1, 0x05],
        [0xf0, 0xfc, 0x18, 0x3f],
        [0xc4, 0xe4, 0x4e, 0x2f]
    ]
    state = shift_rows(state)
    assert state == expected_output

